#include "ft_printf.h"

int	ft_number(int n)
{
	char	s;
	int		i;

	i = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		write (1, "-", 1);
		n = -n;
		i += 1;
	}
	if (n >= 10)
	{
		i += ft_number(n / 10);
		n = n % 10;
	}
	if (n < 10)
		s = n + 48;
	i += 1;
	write (1, &s, 1);
	return (i);
}
